"""
goal_setting.py
----------------
功能說明：
  設定每個自主 Agent 的長期目標列表，方便規劃每日行動。

修仙比喻：
  分神確立每日功課與修煉大計。
"""

# 多個自主 Agent 的長期目標
agent_goals = [
    "每日修煉內功一小時",
    "閱讀武學典籍三篇",
    "模擬戰鬥策略演練",
    "整理門派法寶與丹藥",
    "觀察靈氣波動，調整功法"
]

# -------------------------
# 簡單測試範例
# -------------------------
if __name__ == "__main__":
    for i, goal in enumerate(agent_goals, start=1):
        print(f"分神{i} 目標：{goal}")
