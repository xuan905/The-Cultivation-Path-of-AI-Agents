"""
training_journey.py
-------------------
功能說明：
  設定目標與步驟，規劃任務執行流程。
  簡單的「任務規劃器」，能幫助 AI 把大目標分解成小行動。

修仙比喻：
  下山歷練 —— 修士依照師門傳授的功法，逐步修行，最終完成任務。
  這裡的「歷練」就是任務計畫，AI 需要分階段完成。
"""

def plan_training(goal):
    """
    根據目標，自動拆解成修煉步驟
    """
    steps = []
    if "學習 Python" in goal:
        steps = [
            "安裝 Python 與開發環境",
            "學習基礎語法（變數、迴圈、函式）",
            "練習專案（例如計算器或記事本）",
            "學習模組與套件（如 requests, pandas）",
            "完成一個實用小專案"
        ]
    elif "健身" in goal:
        steps = [
            "制定一週運動計畫",
            "學習正確姿勢（避免走火入魔）",
            "循序漸進增加重量與強度",
            "搭配飲食與休息",
            "檢視成果，調整計畫"
        ]
    else:
        steps = [
            "明確定義目標",
            "拆解為小任務",
            "逐一完成並檢視成果",
            "調整計劃，持續前進"
        ]
    return steps

if __name__ == "__main__":
    print("🧙 開始下山歷練：規劃修煉之路\n")
    goal = input("請輸入你的修煉目標：")
    
    steps = plan_training(goal)
    print(f"\n🎯 你的修煉目標：{goal}")
    print("📜 歷練步驟：")
    for i, step in enumerate(steps, 1):
        print(f"  {i}. {step}")
